def create_fact_wave_events():
    """Create and populate wave_events fact table."""

    print("Building wave_events fact table...")

    # Load cleaned buoy data
    buoy_df = pd.read_csv(f"{PROCESSED_PATH}buoy_data_clean.csv")

    # Load location dimension to get location_ids
    location_df = pd.read_sql("SELECT * FROM dim_location", conn)

    # Merge to get location_id
    buoy_df = buoy_df.merge(
        location_df[["location_key", "location_id"]],
        left_on="location",
        right_on="location_key",
        how="left"
    )

    # Build date_id
    buoy_df["date_id"] = (
        buoy_df["year"].astype(str) +
        buoy_df["month"].astype(str).str.zfill(2) +
        buoy_df["day"].astype(str).str.zfill(2)
    ).astype(int)

    # Nazare benchmark height in meters for comparison
    nazare_height_m = 26.2

    # Build fact table
    fact_df = pd.DataFrame({
        "location_id": buoy_df["location_id"],
        "date_id": buoy_df["date_id"],
        "year": buoy_df["year"],
        "month": buoy_df["month"],
        "wave_height_m": buoy_df["wave_height_m"],
        "dominant_period_sec": buoy_df["dominant_period_sec"],
        "wind_speed_ms": buoy_df["wind_speed_ms"],
        "benchmark_pct_nazare": (
            buoy_df["wave_height_m"] / nazare_height_m * 100
        ).round(2),
        "benchmark_pct_lituya": (
            buoy_df["wave_height_m"] / 524 * 100
        ).round(2),
        "source": buoy_df["source"]
    })

    fact_df.to_sql("wave_events", conn, if_exists="replace", index=False)
    print(f"✓ wave_events created — {len(fact_df)} records")
    return fact_df


