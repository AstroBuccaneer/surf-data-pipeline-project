def create_dim_date():
    """Create and populate date dimension table."""

    print("Building dim_date...")

    dates = pd.date_range(start="2000-01-01", end="2023-12-31", freq="D")

    data = []
    for date in dates:
        data.append({
            "date_id": int(date.strftime("%Y%m%d")),
            "date": date.strftime("%Y-%m-%d"),
            "year": date.year,
            "month": date.month,
            "month_name": date.strftime("%B"),
            "day": date.day,
            "quarter": date.quarter,
            "season": (
                "Winter" if date.month in [12, 1, 2] else
                "Spring" if date.month in [3, 4, 5] else
                "Summer" if date.month in [6, 7, 8] else
                "Fall"
            ),
            "is_hurricane_season": "Yes" if date.month in [6, 7, 8, 9, 10, 11] else "No"
        })

    df = pd.DataFrame(data)
    df.to_sql("dim_date", conn, if_exists="replace", index=False)
    print(f"✓ dim_date created — {len(df)} records")
    return df

